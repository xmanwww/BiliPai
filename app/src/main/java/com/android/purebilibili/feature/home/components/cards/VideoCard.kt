                        //  [ä¿®å¤] ä½¿ç”¨ face URL hashCode ä½œä¸ºç¼“å­˜ key
                        // åŸå› : å†å²è®°å½•çš„ owner.mid å¯èƒ½ä¸ºç©ºï¼Œå¯¼è‡´æ‰€æœ‰å¤´åƒå…±äº«åŒä¸€ç¼“å­˜
